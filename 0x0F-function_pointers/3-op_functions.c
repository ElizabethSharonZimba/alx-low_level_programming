#include <stdio.h>
#include "3-calc.h"
/**
 * op_add - sum of two var
 * @a: first num
 * @b: second num
 * Return: The sum of a and b
 */
int op_add(int a, int b);
{
	return (a + b);
}
/**
 * op_sub - sub a from b
 * @a: int to check
 * @b: sub
 * Return: difference of a and b
 */
int op_sub(int a, int b);
{
	return (a - b);
}
/**
 * op_mul - product of a and b
 * @a: first num
 * @b: second num
 * Return: product of a and b
 */
int op_mul(int a, int b);
{
	return (a * b);
}
/**
 * op_div - div two num a and b
 * @a: num to divide
 * @b: num divide
 * Return: remainder of a and b
 */
int op_div(int a, int b);
{
	return (a / b);
}
/**
 * op_mod - mod of a div
 * @a: first num
 * @b: second num
 * Return: mod of the div
 */
int op_mod(int a, int b);
{
	return (a % b);
}
